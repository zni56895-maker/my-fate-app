// 六亲：兄弟/子孙/妻财/官鬼/父母
export type LiuQin = '兄弟' | '子孙' | '妻财' | '官鬼' | '父母'

/** 根据本宫五行和爻地支五行判定六亲 */
export function getLiuQin(benGongWuXing: string, yaoDiZhi: string): LiuQin {
  const dizhiWx: Record<string, string> = {
    '子':'水','丑':'土','寅':'木','卯':'木','辰':'土','巳':'火',
    '午':'火','未':'土','申':'金','酉':'金','戌':'土','亥':'水',
  }
  const yaoWx = dizhiWx[yaoDiZhi] || '土'
  const gongWx = benGongWuXing

  if (yaoWx === gongWx) return '兄弟'
  // 生我者父母
  const shengMap: Record<string, string> = { '木':'水','火':'木','土':'火','金':'土','水':'金' }
  if (shengMap[yaoWx] === gongWx) return '父母'
  // 我生者子孙
  if (shengMap[gongWx] === yaoWx) return '子孙'
  // 克我者官鬼
  const keMap: Record<string, string> = { '木':'金','火':'水','土':'木','金':'火','水':'土' }
  if (keMap[yaoWx] === gongWx) return '官鬼'
  // 我克者妻财
  return '妻财'
}

// ===== 六兽排列（日干起青龙） =====

export const LIU_SHOU = ['青龙','朱雀','勾陈','螣蛇','白虎','玄武']

/** 根据日干获取六兽起始位置 */
export function getLiuShouOrder(dayGan: string): string[] {
  const start: Record<string, number> = {
    '甲':0,'乙':0,'丙':1,'丁':1,'戊':2,'己':3,'庚':4,'辛':5,'壬':5,'癸':5,
  }
  const idx = start[dayGan] ?? 0
  return [...LIU_SHOU.slice(idx), ...LIU_SHOU.slice(0, idx)]
}

